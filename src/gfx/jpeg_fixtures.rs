//! Embedded JPEG test fixtures (test-only). Regeneration commands
//! (macOS, mozjpeg/libjpeg `cjpeg` from homebrew), from the 16x16
//! gradient PPM whose formula `expected_rgb` mirrors:
//!
//! ```text
//! python3 - <<'PY'
//! with open('grad.ppm','wb') as f:
//!     f.write(b'P6\n16 16\n255\n')
//!     for y in range(16):
//!         for x in range(16):
//!             f.write(bytes([x*255//15, y*255//15, (x+y)*255//30]))
//! with open('gray.pgm','wb') as f:
//!     f.write(b'P5\n16 16\n255\n')
//!     for y in range(16):
//!         for x in range(16):
//!             f.write(bytes([(x*255//15 + y*255//15)//2]))
//! PY
//! cjpeg -quality 92 -sample 1x1 -outfile grad444.jpg grad.ppm
//! cjpeg -quality 92 -sample 2x2 -outfile grad420.jpg grad.ppm
//! cjpeg -quality 92 -sample 2x1 -outfile grad422.jpg grad.ppm
//! cjpeg -quality 92 -grayscale  -outfile gray.jpg    gray.pgm
//! cjpeg -quality 92 -sample 2x2 -restart 1 -outfile grad420rst.jpg grad.ppm
//! cjpeg -quality 92 -progressive -outfile gradprog.jpg grad.ppm
//! ```

/// Ground truth for the 16x16 RGB gradient (the PPM formula).
pub fn expected_rgb(x: u32, y: u32) -> (u8, u8, u8) {
    (
        (x * 255 / 15) as u8,
        (y * 255 / 15) as u8,
        ((x + y) * 255 / 30) as u8,
    )
}

/// Ground truth for the 16x16 gray gradient (the PGM formula).
pub fn expected_gray(x: u32, y: u32) -> u8 {
    ((x * 255 / 15 + y * 255 / 15) / 2) as u8
}

